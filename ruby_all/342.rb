# Consider the number 50.
# 50^2 = 2500 = 2^2× 5^4, so φ(2500) = 2 × 4 × 5^3 = 8 × 5^3 = 2^3× 5^3. ^1
# So 2500 is a square and  φ(2500) is a cube.
# 
# 
